import 'package:flutter/material.dart';

class Fruit {
  final String name;
  final String description;
  final String imageUrl;

  Fruit({
    required this.name,
    required this.description,
    required this.imageUrl,
  });
}


class Animal {
  final String name;
  final String description;
  final String imageUrl;

  Animal({
    required this.name,
    required this.description,
    required this.imageUrl,
  });
}



class Product {
  final String name;
  final String description;
  final String imageUrl;
  final int price;

  Product({
    required this.name,
    required this.description,
    required this.imageUrl,
    required this.price
  });
}


